        private void CariData(){
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM tb_karyawan WHERE " + cmbCari.getSelectedItem().toString() +
                    " LIKE '%" + txtCari.getText() +"%'");
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("NO");
            model.addColumn("Nik");
            model.addColumn("Nama");
            model.addColumn("Jabatan");
            model.addColumn("Alamat");
            model.addColumn("Email");
            model.addColumn("Notelp");
            int no = 1;
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()) {
                Object[] data = {
                  no ++,
                  rs.getString("Nik"),
                  rs.getString("Nama"),
                  rs.getString("Jabatan"),
                  rs.getString("Alamat"),
                  rs.getString("Email"),
                  rs.getString("Notelp")
                };
                model.addRow(data);
                tblData.setModel(model);
            }
        } catch (Exception e) {
        }
            
        }
