    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        try {
            st = cn.createStatement();
            // Aksi validasi data kosong
            if (txtNik.getText().equals("")||
                txtNama.getText().equals("")||
                txtJabatan.getText().equals("")||
                txtAlamat.getText().equals("")||
                txtEmail.getText().equals("")||
                txtNotelp.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Data Tidak Boleh Kosong","validasi data",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
             
            //Aksi simpan data
            if (btnSimpan.getText() == "Simpan"){
                String cek = "SELECT * FROM tb_karyawan WHERE nik = '"+txtNik.getText() +"'";
                rs = st.executeQuery(cek);
                  System.out.println(rs);
                if(rs.next()){
                    JOptionPane.showMessageDialog(null,"NIK Sudah terdaftar !");
                  
                }else{
//                    System.out.println("COBAAAA");
                    String sql = "INSERT INTO tb_karyawan VALUES('"+txtNik.getText()+
                            "','" + txtNama.getText() +
                            "','" + txtJabatan.getText() +
                            "','" + txtAlamat.getText() +
                            "','" + txtEmail.getText() +
                            "','" + txtNotelp.getText()+"')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
                    Bersih();
                    TampilData();
                }
            }else{
            //Aksi Update
                    String update = "UPDATE tb_karyawan SET nama = '" + txtNama.getText() +
                            "', Jabatan ='" + txtJabatan.getText() +
                            "', Alamat = '" + txtAlamat.getText() +
                            "', Email = '" + txtEmail.getText() +
                            "', Notelp = '" + txtNotelp.getText() +
                            "' WHERE Nik ='" + txtNik.getText() + "'";
                    st.executeUpdate(update);
                    JOptionPane.showMessageDialog(null,"Data Berhasil TerUpdate");
                    Bersih();
                    TampilData();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

